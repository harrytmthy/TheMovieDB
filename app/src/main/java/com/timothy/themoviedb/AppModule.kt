package com.timothy.themoviedb

import android.content.Context
import android.os.Debug
import androidx.room.Room
import com.timothy.themoviedb.data.DatabaseConstants.DB_NAME
import com.timothy.themoviedb.data.TheMovieDbDatabase
import dagger.Module
import dagger.Provides
import dagger.hilt.InstallIn
import dagger.hilt.android.qualifiers.ApplicationContext
import dagger.hilt.components.SingletonComponent
import javax.inject.Singleton

@InstallIn(SingletonComponent::class)
@Module
object AppModule {

    @Singleton
    @Provides
    fun provideDatabase(@ApplicationContext context: Context): TheMovieDbDatabase {
        val builder = Room.databaseBuilder(context, TheMovieDbDatabase::class.java, DB_NAME)
            .fallbackToDestructiveMigration()
        if (Debug.isDebuggerConnected()) {
            builder.allowMainThreadQueries()
        }
        return builder.build()
    }

    @Singleton
    @Provides
    fun provideConfigurationDao(db: TheMovieDbDatabase) = db.configurationDao()
}